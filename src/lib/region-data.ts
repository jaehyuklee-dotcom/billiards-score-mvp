/** 시/도 목록 (정렬) */
const SI_ORDER = ["서울", "부산", "대구", "인천", "광주", "대전", "울산", "세종", "경기", "강원", "충북", "충남", "전북", "전남", "경북", "경남", "제주", "기타"];
export const REGION_SI = SI_ORDER.filter((si) => si in REGION_DATA);
export const REGION_HIERARCHY = REGION_DATA;

/** 시/도의 시/군/구 목록 */
export function getRegionGu(si: string): string[] {
  const guMap = REGION_DATA[si];
  if (!guMap) return [];
  return Object.keys(guMap);
}

/** 시/도의 시/군/구에 해당하는 동/읍/면 목록 */
export function getRegionDong(si: string, gu: string): string[] {
  const guMap = REGION_DATA[si];
  if (!guMap) return [];
  return guMap[gu] ?? [];
}
