export const REGION_SI = Object.keys(REGION_HIERARCHY) as string[];

/** 시/도의 시/군/구 목록 */
export function getRegionGu(si: string): string[] {
  const guMap = REGION_HIERARCHY[si];
  if (!guMap) return [];
  return Object.keys(guMap);
}

/** 시/도의 시/군/구에 해당하는 동 목록 */
export function getRegionDong(si: string, gu: string): string[] {
  const guMap = REGION_HIERARCHY[si];
  if (!guMap) return [];
  const dongList = guMap[gu];
  return dongList ?? [];
}
